package bean;

public class FollowFolderBean {

	private int follow_num, mem_num, folder_num;
	private String folder_title, folder_creater;

	public int getFollow_num() {
		return follow_num;
	}

	public void setFollow_num(int follow_num) {
		this.follow_num = follow_num;
	}

	public int getMem_num() {
		return mem_num;
	}

	public void setMem_num(int mem_num) {
		this.mem_num = mem_num;
	}

	public int getFolder_num() {
		return folder_num;
	}

	public void setFolder_num(int folder_num) {
		this.folder_num = folder_num;
	}

	public String getFolder_title() {
		return folder_title;
	}

	public void setFolder_title(String folder_title) {
		this.folder_title = folder_title;
	}

	public String getFolder_creater() {
		return folder_creater;
	}

	public void setFolder_creater(String folder_creater) {
		this.folder_creater = folder_creater;
	}

}
